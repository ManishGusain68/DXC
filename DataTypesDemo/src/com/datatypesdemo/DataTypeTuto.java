package com.datatypesdemo;

public class DataTypeTuto {

	public static void main(String[] args) {
	
		byte b=110;
		
		int i=b;  //implicit type casting
		
		
		int m=100;
		
		byte n=(byte)m;  //explicit type casting
		
		System.out.println(n );
		
		
		double a=50.50;
		
		float f=(float)a; //explicit type casting
		
	
		

	}

}
